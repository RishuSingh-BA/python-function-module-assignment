# Using the Math module for calculation

import math

num = float(input("Enter a number: "))

sqrt_val = math.sqrt(num)
print(f"The square root of {num} is {sqrt_val}")

log_val  = math.log(num)
print(f"The logarithm of {num} is {log_val}")

sine_val = math.sin(num)
print(f"The sine of {num} is {sine_val}")

